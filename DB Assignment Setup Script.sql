-- Creating Tables --
CREATE TABLE Passenger
(
PgrID tinyint NOT NULL,
PgrName varchar(50) NOT NULL,
CabinNo tinyint NOT NULL,
PgrGender char(1) NOT NULL,
PgrDOB date NOT NULL,
PgrEmail varchar(150) NULL,
PRIMARY KEY(PgrID),
CONSTRAINT CK_PgrGender CHECK(PgrGender IN ('M', 'F')),
CONSTRAINT CK_PgrDOB CHECK(PgrDOB <= GETDATE())
)

CREATE TABLE PassengerContact
(
PgrID tinyint NOT NULL, 
PgrContactNo char(10) NOT NULL,
PRIMARY KEY(PgrID, PgrContactNo),
CONSTRAINT FK_PassengerContact_PgrID FOREIGN KEY (PgrID) REFERENCES Passenger(PgrID)
)

CREATE TABLE EventType
(
ETID tinyint NOT NULL,
ETName varchar(50) NOT NULL,
PRIMARY KEY (ETID),
)

CREATE TABLE Event
(
EventID tinyint NOT NULL,
EventName varchar(50) NOT NULL,
EventDescr varchar(100) NOT NULL,
EventLoc varchar(100) NOT NULL,
MinAge tinyint NULL,
MaxAge tinyint NULL,
EventCapacity tinyint NOT NULL,
EventDuration tinyint NOT NULL,
AdultPrice smallmoney NULL,
ChildPrice smallmoney NULL,
ETID tinyint NOT NULL,
PRIMARY KEY (EventID),
CONSTRAINT CK_MinAge CHECK(MinAge >= 0),
CONSTRAINT CK_MaxAge CHECK(MaxAge >= 0),
CONSTRAINT CK_EventCapacity CHECK(EventCapacity > 0),
CONSTRAINT CK_EventDuration CHECK(EventDuration > 0),
CONSTRAINT CK_AdultPrice CHECK(AdultPrice > 0),
CONSTRAINT CK_ChildPrice CHECK(ChildPrice > 0),
CONSTRAINT FK_Event_ETID FOREIGN KEY(ETID) REFERENCES EventType(ETID)
)

CREATE TABLE EventSession
(
EventID tinyint NOT NULL,
SessionNo tinyint NOT NULL,
EventDateTime smalldatetime NOT NULL,
PRIMARY KEY (EventID, SessionNo),
CONSTRAINT FK_EventSession_EventID FOREIGN KEY (EventID) REFERENCES Event(EventID),
)

CREATE TABLE Booking
(
BookingID tinyint NOT NULL,
NoOfAdultTicket tinyint NOT NULL,
NoOfChildTicket tinyint NOT NULL,
AdultSalesPrice smallmoney NOT NULL,
ChildSalesPrice smallmoney NOT NULL,
BookStatus char(1) NOT NULL,
BookDateTime smalldatetime NOT NULL,
PgrID tinyint NOT NULL,
EventID tinyint NOT NULL,
SessionNo tinyint NOT NULL,
PRIMARY KEY(BookingID),
CONSTRAINT CK_AdultSalesPrice CHECK(AdultSalesPrice >= 0),
CONSTRAINT CK_ChildSalesPrice CHECK(ChildSalesPrice >= 0),
CONSTRAINT CK_BookStatus CHECK(BookStatus IN ('P', 'C', 'W')),
CONSTRAINT CK_BookDateTime CHECK(BookDateTime < GETDATE()),
CONSTRAINT FK_Booking_PgrID FOREIGN KEY (PgrID) REFERENCES Passenger(PgrID),
CONSTRAINT FK_Booking_EventID FOREIGN KEY (EventID, SessionNo) REFERENCES EventSession(EventID, SessionNo),
CONSTRAINT FK_Booking_SessionNo FOREIGN KEY (EventID, SessionNo) REFERENCES EventSession(EventID, SessionNo),
)

CREATE TABLE Cuisine
(
cuisineID tinyint NOT NULL, 
cuisineName varchar(150) NOT NULL,
CONSTRAINT PK_Cuisine PRIMARY KEY(cuisineID)
)

CREATE TABLE Ingredient
(
IngredID tinyint NOT NULL,
IngredName varchar(100) NOT NULL,
CONSTRAINT PK_Ingredient PRIMARY KEY(IngredID)
)

CREATE TABLE FoodCategory
(
FcID tinyint NOT NULL,
FcName varchar(50) NOT NULL,
FcDescr varchar(150) NOT NULL,
CONSTRAINT PK_FoodCategory PRIMARY KEY(FcID)
)

CREATE TABLE Eatery
(
EatyID tinyint NOT NULL,
EatyClHr time NOT NULL,
EatyOpHr time NOT NULL,
EatyCapacity tinyint NOT NULL,
EatyLoc varchar(100) NOT NULL,
EatyName varchar(50) NOT NULL,
CONSTRAINT PK_Eatery PRIMARY KEY(EatyID),
CONSTRAINT CK_ClHr_Eatery CHECK(DATEDIFF(hour, EatyClHr, EatyOpHr) > 0),
CONSTRAINT CK_OpHr_Eatery CHECK(DATEDIFF(hour, EatyOpHr, EatyClHr) < 0),
CONSTRAINT CK_Capacity_Eatery CHECK(EatyCapacity >= 0)
)

CREATE TABLE Reservation
(
ReservID tinyint NOT NULL,
ReservStatus char(1) NOT NULL,
NoOfPax tinyint NOT NULL,
ReservDateTime smalldatetime NOT NULL,
RequiredDateTime smalldatetime NOT NULL,
PgrID tinyint NOT NULL,
EatyID tinyint NOT NULL,
PRIMARY KEY(ReservID),
CONSTRAINT CK_ReservStatus CHECK(ReservStatus IN ('C', 'P', 'F')),
CONSTRAINT CK_NoOfPax CHECK(NoOfPax > 0),
CONSTRAINT CK_ReservDateTime CHECK (ReservDateTime <= GETDATE()),
CONSTRAINT FK_Reservation_PgrID FOREIGN KEY (PgrID) REFERENCES Passenger(PgrID),
CONSTRAINT FK_Reservation_EatyID FOREIGN KEY (EatyID) REFERENCES Eatery(EatyID)
)

CREATE TABLE Dish
(
DishID tinyint NOT NULL,
DishName varchar(50) NOT NULL,
DishDescr varchar(150) NOT NULL,
cuisineID tinyint NOT NULL,
EatyID tinyint NOT NULL,
CONSTRAINT PK_Dish PRIMARY KEY(DishID),
CONSTRAINT FK_Dish_cuisineID FOREIGN KEY (cuisineID) REFERENCES Cuisine(cuisineID),
CONSTRAINT FK_Dish_EatyID FOREIGN KEY (EatyID) REFERENCES Eatery(EatyID),
)

CREATE TABLE CSDish
(
DishID tinyint NOT NULL,
Price smallmoney NOT NULL,
CONSTRAINT PK_CSDish PRIMARY KEY(DishID),
CONSTRAINT FK_CSDish_DishID FOREIGN KEY (DishID) REFERENCES Dish(DishID),
CONSTRAINT CK_Price_CSDish CHECK(Price >= 0.00)
)

CREATE TABLE DishFoodCategory
(
DishID tinyint NOT NULL,
FcID tinyint NOT NULL,
CONSTRAINT PK_DishFoodCategory PRIMARY KEY (DishID, FcID),
CONSTRAINT FK_DishFoodCategory_DishID FOREIGN KEY (DishID) REFERENCES Dish(DishID),
CONSTRAINT FK_DishFoodCategory_FcID FOREIGN KEY (FcID) REFERENCES FoodCategory(FcID)
)

CREATE TABLE DishIngredient
(
DishID tinyint NOT NULL,
IngredID tinyint NOT NULL,
CONSTRAINT PK_DishIngredient PRIMARY KEY (DishID, IngredID),
CONSTRAINT FK_DishIngredient_DishID FOREIGN KEY (DishID) REFERENCES Dish(DishID),
CONSTRAINT FK_DishIngredient_IngredID FOREIGN KEY (IngredID) REFERENCES Ingredient(IngredID)
)

CREATE TABLE Orders
(
PgrID tinyint NOT NULL,
DishID tinyint NOT NULL,
OrderDateTime smalldatetime NOT NULL,
OrderPrice smallmoney NOT NULL,
OrderQty tinyint NOT NULL,
DeliverTo tinyint NOT NULL,
DelDateTime smalldatetime NOT NULL,
CONSTRAINT PK_Orders PRIMARY KEY(PgrID, DishID, OrderDateTime),
CONSTRAINT FK_Orders_PgrID FOREIGN KEY (PgrID) REFERENCES Passenger(PgrID),
CONSTRAINT FK_Orders_DishID FOREIGN KEY (DishID) REFERENCES Dish(DishID),
CONSTRAINT CK_OrPrice_Orders CHECK(OrderPrice >= 0.00),
CONSTRAINT CK_OrQty_Orders CHECK(OrderQty > 0),
CONSTRAINT CK_OrDate_Orders CHECK(OrderDateTime < DelDateTime),
CONSTRAINT CK_DelDate_Orders CHECK(DelDateTime > OrderDateTime)
)

